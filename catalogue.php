        // Assuming you have jQuery library loaded for simplicity

        // Add an event listener to the form submit
        function handleFormSubmit(event, formId) {
            event.preventDefault(); // Prevent the default form submission
            myForms(formId);
        }

        function myForms(formId) {
            var formSelector = '#' + formId;
            var formDataArray = $(formSelector).serializeArray();
            var pdfUrl = formDataArray[1].value;

            $.ajax({
                type: 'POST',
                url: 'mail.php',
                data: $(formSelector).serialize(),
                success: function(response) {
                    if (response === '1') {
                        triggerDownload(pdfUrl, pdfUrl);
                        $('#exampleModal' + formId).modal('hide');
                    } else {
                        alert('Error sending email. Please try again.');
                    }
                },
                error: function() {
                    alert('An error occurred. Please try again.');
                }
            });
        }

        function triggerDownload(pdfUrl, fileName) {
            var link = document.createElement('a');
            link.href = pdfUrl;
            link.target = '_blank'; // Open the PDF in a new tab
            link.download = fileName;
            link.click();
        }
    </script>
